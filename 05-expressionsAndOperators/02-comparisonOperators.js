// Comparison Ooperators

// Equal same quantity not quality
'3' == 3;

// Strict equal same quantity and quality
3 === 3;

// Not equal
'3' != 4;

// Strictly not equal
'3' !== 3;

// Greater than
3 > 2;

// less than
3 < 4;

// greater or equal
3 >= 2;

// less or equal
3 <= 4;

// and
2 && 3;

// or
3 || 4;
